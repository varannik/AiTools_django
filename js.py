def searchInDictOption(data, title):

    for i in data['fields']:
        if i['label'] == title:
            valList = i['value']
            options = i['options']
            res = []
            for v in valList:
                for o in options:
                    if o['id']==v:
                        res.append(o['text'])
            resStr = str(res).replace("[", "").replace("]","").replace("'", "")
            return resStr




